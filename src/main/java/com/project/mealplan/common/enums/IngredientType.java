package com.project.mealplan.common.enums;

public enum IngredientType {
    VEGETABLE,       // Rau, củ (broccoli, carrot, spinach)
    FRUIT,           // Trái cây (apple, banana, orange)
    GRAIN,           // Ngũ cốc (rice, oats, bread, pasta)
    LEGUME,          // Họ đậu (beans, lentils, chickpeas, peas)
    MEAT,            // Thịt đỏ, thịt trắng (chicken, beef, pork)
    SEAFOOD,         // Hải sản (fish, shrimp, crab)
    EGG,             // Trứng (chicken egg, duck egg)
    DAIRY,           // Sữa và chế phẩm (milk, cheese, yogurt)
    NUT_AND_SEED,    // Hạt và quả khô (almond, chia seed, peanut)
    OIL_AND_FAT,     // Dầu mỡ (olive oil, butter, lard)
    BEVERAGE,        // Đồ uống (juice, coffee, milk tea)
    SEASONING,       // Gia vị, nước chấm (salt, soy sauce, pepper)
    OTHER            // Khác (không rõ phân loại)
}


